package ch05_variable

fun main() {
    /*
    상수 : 변하지 않는 변수
    리터럴 : 변하지 않는 값
    */

    val PI = 3.14 // PI : 상수, 3.13 : 리터럴(변하지 않는 데이터 값)
    var msg = "Hi" // "Hi" : 리터럴(변하지 않는 데이터 값)

    println(1234) // 정수 리터럴
    println(3.14F) // 실수 리터럴
    println('A') // char literal
    println("HELLO") // String literal
}